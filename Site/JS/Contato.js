function abrir(){
    var t=document.getElementById("txt1")
    t.style.display="block"

    var b=document.getElementById("btn2")
    b.style.display="inline"
    b.style.cursor="pointer"
}
function fechar(){
    var t=document.getElementById("txt1")
    t.style.display="none"

    var b=document.getElementById("btn2")
    b.style.display="none"
}